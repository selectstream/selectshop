import 'dotenv/config';
import { Client } from '@notionhq/client';

const {
  PINTEREST_ACCESS_TOKEN,
  PINTEREST_BOARD_ID,
  NOTION_TOKEN,
  DATABASE_ID
} = process.env;

const notion = new Client({ auth: NOTION_TOKEN });
const DRY_RUN = false; // Set to false for live production

/**
 * PINTEREST DISTRIBUTOR ENGINE
 * The autonomous bridge between Notion curation and Pinterest traffic.
 */
async function distribute() {
  console.log(`🚀 Starting Pinterest Distribution Swarm... ${DRY_RUN ? "[DRY RUN ACTIVE]" : ""}`);

  if (!DRY_RUN && (!PINTEREST_ACCESS_TOKEN || !PINTEREST_BOARD_ID)) {
    console.error("❌ Missing Pinterest API Credentials. Distribution Halted.");
    return;
  }

  // 1. Fetch Unpinned Products from Notion
  const products = await fetchUnpinnedProducts();
  console.log(`📦 Found ${products.length} items ready for distribution.`);

  for (const product of products) {
    try {
      console.log(`\n🖌️  Processing: ${product.title}`);
      
      // 2. Execute Pin to Pinterest
      const success = await createPin(product);
      
      if (success) {
        // 3. Update Notion (Mark as Pinned)
        await markAsPinned(product.id);
        console.log(`✅ Successfully distributed ${product.title} to Pinterest.`);
      }
    } catch (error) {
      console.error(`❌ Distribution Failed for ${product.title}:`, error.message);
    }
  }

  console.log("\n💎 Distribution Swarm Cycle Complete.");
}

async function fetchUnpinnedProducts() {
  const response = await fetch(`https://api.notion.com/v1/databases/${DATABASE_ID}/query`, {
    method: 'POST',
    headers: {
      'Authorization': `Bearer ${NOTION_TOKEN}`,
      'Content-Type': 'application/json',
      'Notion-Version': '2022-06-28'
    },
    body: JSON.stringify({
      filter: {
        and: [
          { property: 'Status', status: { equals: 'Live' } },
          { property: 'Pinned to Social', checkbox: { equals: false } }
        ]
      }
    })
  });

  const data = await response.json();
  if (!data.results) {
    console.error("❌ Notion API Error:", data);
    return [];
  }
  return data.results.map(page => {
    const p = page.properties;
    return {
      id: page.id,
      title: p['Product Name']?.title[0]?.plain_text,
      slug: p['Slug']?.rich_text[0]?.plain_text,
      notes: p['Notes']?.rich_text[0]?.plain_text,
      categories: p['Stream Category']?.multi_select?.map(s => s.name) || [],
      // Priority logic: uses Cloudinary path generated by our Image Engine
      imageUrl: `https://res.cloudinary.com/${process.env.CLOUDINARY_CLOUD_NAME}/image/upload/selectstream/stream/${p['Slug']?.rich_text[0]?.plain_text}-pin.jpg`
    };
  });
}

/**
 * BOARD MAPPING INTELLIGENCE
 * Autonomously selects the target board based on product categories.
 */
const BOARD_MAP = {
  'Audio': '1120270544749497925',
  'Workspace': '1120270544749497926',
  'Productivity': '1120270544749497928',
  'Organization': '1120270544749497922',
  'Deep Work': '1120270544749497928', // Fallback to Elite Curation
  'Biohacking': '1120270544749497922'  // Fallback to Minimalist Kit
};

async function createPin(product) {
  // Determine Board ID based on primary category
  const category = product.categories?.[0] || 'Workspace';
  const targetBoardId = BOARD_MAP[category] || process.env.PINTEREST_BOARD_ID || 'DEFAULT_BOARD';

  if (DRY_RUN) {
    console.log(`   [DRY RUN] Would pin: ${product.title}`);
    console.log(`   [DRY RUN] Target Board: ${category} (ID: ${targetBoardId})`);
    console.log(`   [DRY RUN] Asset: ${product.imageUrl}`);
    return true;
  }

  const response = await fetch('https://api.pinterest.com/v5/pins', {
    method: 'POST',
    headers: {
      'Authorization': `Bearer ${PINTEREST_ACCESS_TOKEN}`,
      'Content-Type': 'application/json'
    },
    body: JSON.stringify({
      title: `${product.title} | Matte Black Essential`,
      description: `${product.notes} #MinimalistTech #DeskSetup #MatteBlack (Paid Link)`,
      link: `https://selectstreamshop.com/products/${product.slug}`,
      board_id: targetBoardId,
      media_source: {
        source_type: 'image_url',
        url: product.imageUrl
      }
    })
  });

  const data = await response.json();
  if (response.ok) return true;
  
  console.error(`   ❌ Pinterest API Error: ${data.message}`);
  return false;
}

async function markAsPinned(pageId) {
  if (DRY_RUN) {
    console.log(`   [DRY RUN] Would mark Notion page ${pageId} as pinned.`);
    return;
  }

  await fetch(`https://api.notion.com/v1/pages/${pageId}`, {
    method: 'PATCH',
    headers: {
      'Authorization': `Bearer ${NOTION_TOKEN}`,
      'Content-Type': 'application/json',
      'Notion-Version': '2022-06-28'
    },
    body: JSON.stringify({
      properties: {
        'Pinned to Social': {
          checkbox: true
        }
      }
    })
  });
  console.log(`✅ Marked in Notion as Pinned.`);
}

distribute();
